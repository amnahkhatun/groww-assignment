import logo from "./logo.svg";
import "./App.css";
import Home from "./Home";
import NotFound from "./NotFound";
import StateComponent from "./State";

import { BrowserRouter as Router, Route, Switch } from "react-router-dom";

function App() {
  return (
    <Router>
      <div className="App">
        <Switch>
          <Route exact path="/">
            <Home />
          </Route>
          <Route path="/state/:code">
            <StateComponent />
          </Route>
          <Route path="*">
            <NotFound />
          </Route>
        </Switch>
      </div>
    </Router>
  );
}

export default App;
